map_list = map.splitlines()


def trees_counter(map, jump, vertical_jump):
    # pomodniczo dla obliczenia długości wiersza i liczby wierszy
    length = len(map_list[0])
    # list_lenght = len(map_list)

    # liczba trafionych drzew
    trees_hit = 0
    # określenie pozycji poziomej
    plane_position = 0
    # pomocniczo dla określenia czy sprawdzamy daną iterację
    numb_of_iteration = 0


    for line in map:
        if numb_of_iteration % vertical_jump == 0:
            if plane_position >= length:
                plane_position = plane_position - length

            if line[plane_position] == '#':
                trees_hit += 1

            plane_position += jump
            numb_of_iteration += 1
        else:
            numb_of_iteration += 1

    # print(trees_hit)
    return trees_hit

